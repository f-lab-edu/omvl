package com.omvl.omvl.controller;

public class MemberForm {

	private String id;
	private String password;
	private String name;
	private int gender;
	private int age;

	public String getId() {
		return id;
	}

	public String getPassword() {
		return password;
	}

	public String getName() {
		return name;
	}

	public int getGender() {
		return gender;
	}

	public int getAge() {
		return age;
	}

}
